<?php
error_reporting(false);
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Settings
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

$Message_Success = '<p class="Data-Form-Popup-Success">Сообщение отправлено!</p>';
$Message_Error = '<p class="Data-Form-Popup-Error">Ошибка отправки сообщения!</p>';
$Redirect = false; // You can set redirect URL, for example "/actions/success.php"



////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Set Post Data
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

$Mail_To_Admin = $_POST['mailto_admin'];
$Mail_To_Client = $_POST['maillto_client'];
$Subject = $_POST['subject'];
$Message_To_Client = $_POST['message_to_client'];
$Message_To_Admin = $_POST['message_to_admin'];

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Data To Mail
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

$Headers = "Content-type: text/html; charset=utf-8 \r\n
            From: $Subject <$Mail_To_Admin>\r\n";


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Send Email To Admin
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

if (mail($Mail_To_Admin, $Subject, $Message_To_Admin, $Headers)) {
    echo $Message_Success;
} else {
    echo $Message_Error;
}

if ($Redirect) {
    header('Location: ' . $Redirect);
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Send Email To Client
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

if ($Message_To_Client) {
    mail($Mail_To_Client, $Subject, $Message_To_Client, $Headers);
}


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Send Email To Client
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

if ($Redirect) {
    header('Location: ' . $Redirect);
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////